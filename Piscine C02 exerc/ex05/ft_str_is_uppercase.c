#include <stdio.h>

int	ft_str_is_uppercase(char *str)
{
	int	i;
	
	if (!str)
		return (1);
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 65 && str[i] <= 90))
			return (0);
		i++;
	}
	return (1);
}

int	main(void)
{
	char	*str = "CASA";
	printf("%i\n", ft_str_is_uppercase(str));
	return (0);
}
