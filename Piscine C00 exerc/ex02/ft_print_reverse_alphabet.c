#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	z;
	
	z = 122;
	while (z >= 97)
	{
		ft_putchar(z);
		z--;
	}
}
